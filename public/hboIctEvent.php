      <H1 style="font-size: 40px;">HBO-ICT job event</H1>
      <Pre style="font-size: x-large ;">
We zijn op vrijdag 4 September naar het HBO-ICT job event geweest in Utrecht.
Hier stonden veel bedrijven die op de een of andere manier met ICT bezig zijn.
Je kon naar ze toe gaan en dan gaven ze informatie over hun bedrijf en wat ICT ermee van doen heeft.
Wat ook zeer ineresant was het feit dat we overal onze contact gegevens hebben afgegeven.
Daar waren ze zeer tevreden over en dan is de overgang naar een stage in het 3de jaar gemmakelijker.
Een aantal bedrijven die er stonden zijn:

1.  Chipsoft
2.  CBS
3.  Motion10
4.  KPN ICT Consulting
5.  Cegeka
6.  Accenture
7.  Betabit
8.  Achmea
9.  Capgemini
10. Team Rockstars IT

